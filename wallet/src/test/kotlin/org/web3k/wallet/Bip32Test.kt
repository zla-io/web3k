/**
 * Test batch for key derivation from seed given a path.
 *
 * The test vectors used are the ones described in [bip32](https://github.com/bitcoin/bips/blob/master/bip-0032.mediawiki)
 */
object Bip32Test : Spek({

    given("Bip32") {

        it("contains key pair") {
            val simpleKey = generateKey(ByteArray(0), "m/")
            assertThat(simpleKey.keyPair).isNotNull()
        }

        on("samples") {

            testData.forEach { sample ->

                val derivedKey = generateKey(sample.seed.hexToByteArray(), sample.path)

                it("resolve public key for $sample") {
                    val obtainedPub = derivedKey.serialize(true)
                    assertThat(obtainedPub).isEqualTo(sample.expectedPublicKey)
                }

                it("resolve private key for $sample") {
                    val obtainedPrv = derivedKey.serialize()
                    assertThat(obtainedPrv).isEqualTo(sample.expectedPrivateKey)
                }

                it("match private key extended key for $sample") {
                    val parsedPrv = sample.expectedPrivateKey.toExtendedKey()
                    assertThat(parsedPrv).isEqualTo(derivedKey)
                }
            }
        }

        // Check that the first address generated by a mnemonic/password used in mycrypto.com is resolved
        on("samples from mycrypto.com") {
            // First seed sample in test-vectors.txt, resolved by mycrypto.com with related mnemonic/passphrase(=TREZOR)
            val seed = "c55257c360c07c72029aebc1b53c05ed0362ada38ead3e3e9efa3708e53495531f09a6987599d18264c1e1c92f2cf141630c7a3c4ab7c81b2f001698e7463b04"
            val samples = listOf(
                    listOf(
                            "m/44'/60'/0'/0/0",
                            "9c32f71d4db8fb9e1a58b0a80df79935e7256fa6",
                            "62f1d86b246c81bdd8f6c166d56896a4a5e1eddbcaebe06480e5c0bc74c28224"
                    ),
                    listOf(
                            "m/44'/60'/0'/0/1",
                            "7af7283bd1462c3b957e8fac28dc19cbbf2fadfe",
                            "49ee230b1605382ac1c40079191bca937fc30e8c2fa845b7de27a96ffcc4ddbf"
                    )
            )

            samples.forEach { sample ->
                val derivedKey = generateKey(seed.hexToByteArray(), sample.first())
                val expectedAddress = sample[1]
                val expectedPrivateKey = sample[2]

                it("has expected address for $sample") {
                    assertThat(derivedKey.keyPair.publicKey.publicKeyToAddress().clean0xPrefix())
                            .isEqualTo(expectedAddress)
                }

                it("has expected private key for $sample") {
                    assertThat(derivedKey.keyPair.privateKey.toHexStringNoPrefix())
                            .isEqualTo(expectedPrivateKey)
                }
            }
        }
    }
})

